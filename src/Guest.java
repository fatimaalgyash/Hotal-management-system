/**
 * Simple Guest class.
 */
public class Guest {

    private String name;

    /**
     * Constructor with name.
     */
    public Guest(String name) {
        this.name = name;
    }
    /**
     * Gets the name.
     */
    public String getName() {
        return name;
    }

    /**
     * Sets the name.
     */
    public void setName(String name) {
        this.name = name;
    }
}
